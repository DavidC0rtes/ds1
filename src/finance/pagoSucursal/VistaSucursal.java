    private void fileChooserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fileChooserActionPerformed
    	File bankFile = fileChooser.getSelectedFile();
    	if (bankFile != null) {
        	control = new ControlSucursal(bankFile, loggedUser.getUserID());
        	
        	int result = control.validateFile();
        	
        	if (result >=0) {
        		JOptionPane.showMessageDialog(
        				this, "Error en el archivo cargado. "
        						+ "Por favor abralo manualmente y revise "
        						+ "los contenidos desde la l√≠nea "+result+1+" hasta la "+result+4+".\n"
        						+ " Edite el archivo manualmente y vuelva a intentarlo.",
        						"Advertencia", JOptionPane.WARNING_MESSAGE 
        				);
        	} else {
        		JOptionPane.showMessageDialog(this, "Pagos registrados correctamente.");
        	}
        }
    }//GEN-LAST:event_fileChooserActionPerformed

